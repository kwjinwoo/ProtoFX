class TestGraphAddInput:
    """Verify Graph.add_input creates and registers a GRAPH_INPUT Value."""

    def test_returns_value(self) -> None:
        """add_input must return a Value."""
        g = Graph()
        v = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(2, 3)))
        assert v is not None

    def test_value_kind_is_graph_input(self) -> None:
        """Returned Value must have GRAPH_INPUT kind."""
        g = Graph()
        v = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(1,)))
        assert v.kind == ValueKind.GRAPH_INPUT

    def test_value_has_auto_id(self) -> None:
        """Value id is auto-generated by Graph."""
        g = Graph()
        v = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(1,)))
        assert isinstance(v.id, str)
        assert len(v.id) > 0

    def test_value_registered_in_inputs(self) -> None:
        """Value is appended to graph.inputs."""
        g = Graph()
        v = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(2,)))
        assert v in g.inputs

    def test_value_registered_in_values(self) -> None:
        """Value is registered in the internal value registry."""
        g = Graph()
        g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(2,)))
        assert g.value_count == 1

    def test_auto_id_increments(self) -> None:
        """Each add_input produces a distinct id."""
        g = Graph()
        v0 = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(1,)))
        v1 = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(2,)))
        assert v0.id != v1.id

    def test_name_preserved(self) -> None:
        """Optional name is stored on the Value."""
        g = Graph()
        v = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(1,)), name="input_0")
        assert v.name == "input_0"

    def test_producer_is_none(self) -> None:
        """Graph inputs have no producer."""
        g = Graph()
        v = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(1,)))
        assert v.producer is None

    def test_users_empty(self) -> None:
        """Newly created input has no users."""
        g = Graph()
        v = g.add_input(tensor_type=TensorType(dtype=DType.FLOAT32, shape=(1,)))
        assert v.users == []


